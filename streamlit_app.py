# ─── HELPER FUNCTIONS ──────────────────────────────────────────────────────
def get_satellite_image(lat, lng):
    """
    ฟังก์ชันดึงภาพดาวเทียมรอบพิกัดโรงงาน
    (ดึงจาก Google Static Maps API หรือ Earth Engine ตามที่ทำไว้ใน notebook)
    """
    # สมมุติตัวอย่างเป็น array เปล่าขนาดที่ U-Net ต้องการ (เช่น 256x256x3)
    # ในงานจริงต้องใช้โค้ดดึงภาพจากขั้นตอนที่ 2 ของไฟล์เดิมมาใส่ตรงนี้ครับ
    mock_img = np.zeros((256, 256, 3), dtype=np.uint8)
    return mock_img

def predict_roof_area(image, model):
    """
    ใช้ U-Net ทำ Segmentation หาพื้นที่หลังคา
    """
    # 1. Preprocess ภาพให้เข้ากับที่โมเดลต้องการ
    img_input = np.expand_dims(image / 255.0, axis=0) # Normalize และเพิ่ม Batch dimension
    # 2. Predict Mask
    mask = model.predict(img_input, verbose=0)[0]
    binary_mask = (mask > 0.5).astype(np.uint8)

    # 3. คำนวณพื้นที่ (ตร.ม.) ตามอัตราส่วนความละเอียดภาพ (Resolution) ของคุณ
    # สมมุติค่าตัวคูณพิกเซลละ 0.5 เมตร -> 1 พิกเซล = 0.25 ตร.ม.
    pixel_resolution = 0.25
    roof_pixels = np.sum(binary_mask)
    calculated_area = roof_pixels * pixel_resolution

    return binary_mask, calculated_area

def predict_solar_irradiance(gru_model, scaler):
    """
    ใช้ GRU พยากรณ์ค่ารังสีแสงอาทิตย์ล่วงหน้า
    """
    # ดึงข้อมูลรังสีฯ ย้อนหลังของพิกัดนั้น (X_va_dl ของคุณ)
    # ในที่นี้สมมุติข้อมูล Input ขนาด (1, timesteps, features)
    mock_input_scaled = np.random.rand(1, 12, 1) # สมมุติใช้ 12 เดือนย้อนหลัง

    # พยากรณ์
    pred_scaled = gru_model.predict(mock_input_scaled, verbose=0)

    # Inverse Transform กลับเป็นค่าจริง (รังสีแผ่พลังงาน)
    real_pred = scaler.inverse_transform(pred_scaled.reshape(-1, 1)).flatten()
    return real_pred

