# ─── MAIN DASHBOARD ────────────────────────────────────────────────────────
st.title("🛰️ Industrial Solar Rooftop Scout Dashboard")
st.write("ระบบ AI วิเคราะห์ศักยภาพและประเมินความคุ้มค่าการติดตั้งโซลาร์เซลล์สำหรับโรงงานอุตสาหกรรม")

if analyze_btn:
    with st.spinner("🔄 ระบบกำลังประมวลผลโมเดล AI..."):

        # 1. แสดงแผนที่พิกัดโรงงาน
        col1, col2 = st.columns([1, 1])
        with col1:
            st.subheader("🗺️ ที่ตั้งโรงงาน")
            m = folium.Map(location=[lat_input, lng_input], zoom_start=17)
            folium.Marker([lat_input, lng_input], popup="โรงงานที่เลือก").add_to(m)
            st_folium(m, width=500, height=300)

        # 2. ทำงานกับ U-Net (พื้นที่หลังคา)
        sat_img = get_satellite_image(lat_input, lng_input)
        roof_mask, roof_area = predict_roof_area(sat_img, unet_model)

        with col2:
            st.subheader("📐 ผลการวิเคราะห์พื้นที่หลังคา (U-Net)")
            st.metric(label="พื้นที่หลังคาที่ตรวจพบ", value=f"{roof_area:,.2f} ตร.ม.")

            # แสดงภาพ Mask ที่ U-Net ตัดได้
            fig, ax = plt.subplots(1, 2, figsize=(6, 3))
            ax[0].imshow(sat_img)
            ax[0].set_title("Original Image")
            ax[0].axis('off')
            ax[1].imshow(roof_mask.squeeze(), cmap='gray')
            ax[1].set_title("Predicted Roof Mask")
            ax[1].axis('off')
            st.pyplot(fig)

        st.markdown("---")

        # 3. 工作กับ GRU (พยากรณ์รังสีอาทิตย์และคำนวณกำลังผลิต)
        st.subheader("☀️ การพยากรณ์ค่ารังสีแสงอาทิตย์ล่วงหน้า (GRU)")

        forecast_values = predict_solar_irradiance(gru_model, scaler)

        # สร้าง DataFrame แสดงผลกราฟ
        months = [f"Month +{i+1}" for i in range(len(forecast_values))]
        df_forecast = pd.DataFrame({
            'Month': months,
            'Predicted Irradiance (kWh/m²/day)': forecast_values
        })

        col3, col4 = st.columns([2, 1])
        with col3:
            # พล็อตแนวโน้มรังสีแสงอาทิตย์
            st.line_chart(df_forecast.set_index('Month'))

        with col4:
            # คำนวณผลประโยชน์/ขนาดการติดตั้งเบื้องต้น (Financial & Technical ROI)
            st.markdown("### 📊 ประมาณการติดตั้งโซลาร์เซลล์")

            # สูตรคำนวณคร่าวๆ: พื้นที่หลังคา 10 ตร.ม. ติดตั้งได้ประมาณ 1 kWp
            potential_kwp = roof_area / 10.0
            avg_irradiance = np.mean(forecast_values)

            # พลังงานที่ผลิตได้ต่อปีโดยประมาณ = kWp * ค่ารังสีเฉลี่ย * PR(0.75) * 365 วัน
            annual_energy = potential_kwp * avg_irradiance * 0.75 * 365

            st.write(f"**ขนาดกำลังผลิตสูงสุดที่ติดตั้งได้:** {potential_kwp:,.2f} kWp")
            st.write(f"**ค่ารังสีแสงอาทิตย์เฉลี่ย:** {avg_irradiance:.2f} kWh/m²/day")
            st.write(f"**คาดการณ์พลังงานที่ผลิตได้:** {annual_energy:,.2f} kWh/ปี")

